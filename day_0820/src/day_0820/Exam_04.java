package Day_0820;

/* 자바의 표준 출력 -> 모니터 출력
 * System.out.println() -> \n 자동 줄바꿈이 들어가있음
 * System.out.print() -> \n 자동 줄바꿈이 빠져있음
 * println -> printline
 * */
public class Exam_04 {

	public static void main(String[] args) {
		System.out.println("--------------------");
		System.out.println("print연습");
		System.out.print("*");
		System.out.print("**");
		System.out.print("***");
		System.out.println();
		System.out.println("--------------------");
		System.out.println("\\t연습");
		System.out.print("*\t");
		System.out.print("**\t");
		System.out.print("***\t");
		System.out.println();
		System.out.println("--------------------");
		System.out.println("\\n연습");
		System.out.print("*\n");
		System.out.print("**\n");
		System.out.print("***\n");
		System.out.println("--------------------");
		System.out.println("ln연습");
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
		System.out.println("--------------------");
		System.out.println("\\t연습");
		System.out.println("번호	이름	국어");
		System.out.println("번호\t이름\t국어");
		System.out.println("--------------------");
		System.out.println("\\s연습");
		System.out.println("번호 이름 국어");
		System.out.println("번호\s이름\s국어");
	}
}
